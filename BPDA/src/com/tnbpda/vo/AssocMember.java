package com.tnbpda.vo;

public class AssocMember {

	private int ccNo;
	private String role;
	/**
	 * @return the ccNo
	 */
	public int getCcNo() {
		return ccNo;
	}
	/**
	 * @param ccNo the ccNo to set
	 */
	public void setCcNo(int ccNo) {
		this.ccNo = ccNo;
	}
	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}
	/**
	 * @param role the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}
	
}
